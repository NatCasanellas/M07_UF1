#Exercici de loops.
#Mostrar els umeros de l'1 al 100 amb un while

#WHILE
#inicialitzar la variable
i = 1

#Bucle while per mostrar els numeros de l'1 al 100
while i <= 100:
    print(i)
    i += 1 #incrementa la variable

#FOR
#Bucle fos per mostrar els numeros de l'1 al 100
for i in range(1, 101):
    print(i)    